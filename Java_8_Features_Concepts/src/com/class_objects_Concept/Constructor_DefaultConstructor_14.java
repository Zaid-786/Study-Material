package com.class_objects_Concept;


/* Constructor is a Special type method which is used to initialized the Object.
 	Constructor name must be same as its class name
 	there are two types of Constructor 
   		1) Default Constructor 
   		2) Parameterized Constructor */

// this is example of Default Constructor 


public class Constructor_DefaultConstructor_14
{
	public Constructor_DefaultConstructor_14()
	{
		System.out.println("this is Default-Constructor example...");
	}
	
	public static void main(String[] args)
	{
		Constructor_DefaultConstructor_14  a=new Constructor_DefaultConstructor_14();
	}

}



/* RULE:-
 *  if there is no constructor in a class, Compiler automatically creates a Default Constructor .
 * */
