package com.class_objects_Concept;



//this is Predefined method example
// predefined Method means that method is already written somewhere among any predefined class by java.
// write a program to find maximum number between two numbers by using predefined mehtod.


public class Method_PredefinedMethod_9 
{
	public static void main(String[] args)
	{
			int num=Math.max(10, 20);
			
			System.out.println("max numbers are between two number = "+num);
	}

}



//output:- max numbers are between two number = 20
